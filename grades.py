import pandas as pd
import numpy as np

# 1. Создаем DataFrame с данными
data = {
    'Имя': ['Алексей', 'Мария', 'Иван', 'Анна', 'Дмитрий', 'Ольга', 'Павел', 'Елена', 'Сергей', 'Наталья'],
    'Математика': [4, 3, 5, 2, 4, 5, 3, 4, 5, 2],
    'Физика': [5, 3, 4, 4, 3, 4, 5, 2, 4, 3],
    'Химия': [3, 4, 2, 5, 4, 3, 4, 5, 3, 4],
    'Биология': [4, 5, 3, 4, 2, 4, 5, 3, 4, 4],
    'Литература': [3, 4, 5, 3, 4, 5, 2, 4, 3, 5]
}

df = pd.DataFrame(data)

# 2. Выводим первые несколько строк DataFrame
print("Первые несколько строк DataFrame:")
print(df.head())

# 3. Вычисляем среднюю оценку по каждому предмету
mean_scores = df.mean(numeric_only=True)
print("\nСредняя оценка по каждому предмету:")
print(mean_scores)

# 4. Вычисляем медианную оценку по каждому предмету
median_scores = df.median(numeric_only=True)
print("\nМедианная оценка по каждому предмету:")
print(median_scores)

# 5. Вычисляем Q1 и Q3 для оценок по математике
Q1_math = df['Математика'].quantile(0.25)
Q3_math = df['Математика'].quantile(0.75)
print(f"\nQ1 для математики: {Q1_math}")
print(f"Q3 для математики: {Q3_math}")

# Вычисляем IQR (интерквартильный размах) для оценок по математике
IQR_math = Q3_math - Q1_math
print(f"IQR для математики: {IQR_math}")

# 6. Вычисляем стандартное отклонение по каждому предмету
std_deviation = df.std(numeric_only=True)
print("\nСтандартное отклонение по каждому предмету:")
print(std_deviation)
